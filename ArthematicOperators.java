package AB58;

public class ArthematicOperators

{

	public static void main(String[] args)
	{
		{
			int a=10;
			int b=20;
			int c=a+b;
			{
				System.out.println(c);
			}
			
			int d=10;
			int e=20;
			int f=d-e;
			{
				System.out.println(f);
			}
			
			int g=10;
			int h=20;
			int i=g*h;
			{
				System.out.println(i);
			}
			
			int j=10;
			int k=20;
			int l=k/j;
			{
				System.out.println(l);
			}
			
			int m=10;
			int n=20;
			int o=n%m;
			{
				System.out.println(o);
			}
		}
		
	}
}