package AB58;

public class SwitchCasePrograme 

{

	public static void main(String[] args)
	
	{
		switch(500)
		
		{
		
		case 1 : System.out.println("Launch google browser");
		
		         break;
		         
		         
		
		
		case 2 : System.out.println("Launch firefox browser");
		
        break;         
		         
        
		case 3 : System.out.println("Launch Mozilla browser");
		
        break;
        
        
        default:
        
        
        {
        
        System.out.println("Sorry your input is wrong please select 1,2,3,4");
		
       
		}
		
		
		

	}

}
	
}
